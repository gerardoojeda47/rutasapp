  /// Obtiene barrios por comuna específica
  static List<PopayanNeighborhood> getNeighborhoodsByComuna(String comuna) {
    return _neighborhoods.where((n) => n.comuna == comuna).toList();
  }

  /// Obtiene todas las áreas rurales (corregimientos y veredas)
  static List<PopayanNeighborhood> getRuralAreas() {
    return _neighborhoods.where((n) => n.comuna == 'Sector Rural').toList();
  }

  /// Busca barrios por texto
  static List<PopayanNeighborhood> searchNeighborhoods(String query) {
    if (query.isEmpty) return [];

    final queryLower = query.toLowerCase();
    return _neighborhoods.where((neighborhood) {
      return neighborhood.name.toLowerCase().contains(queryLower) ||
          neighborhood.keywords
              .any((keyword) => keyword.toLowerCase().contains(queryLower));
    }).toList();
  }

